// Bucle para iterar del 1 al 30
for (let i = 1; i <= 30; i++) {
    // Creamos una cadena que contenga el número `i` repetido `i` veces
    let linea = i.toString().repeat(i);
    console.log(linea); // ver la piramide en la consola
}

//i.toString(): Convierte el número i en una cadena.
//.repeat(i): Repite la cadena i veces.